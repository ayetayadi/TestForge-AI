class RiskResponse(RiskBase):
    """Schema for API responses."""

    id: str
    user_story_id: Optional[str] = None
    test_plan_id: Optional[str] = None
    user_story_key: Optional[str] = Field(
        None,
        description="Jira issue key of the associated User Story"
    )
    user_story_title: Optional[str] = Field(
        None,
        description="Title of the associated User Story"
    )
    risk_score: int = Field(
        ...,
        ge=1,
        le=25,
        description="Risk Score = P × I (1-25)"
    )
    level: str = Field(
        ...,
        pattern="^(critical|high|medium|low)$",
        description="Risk level classification"
    )
    created_at: datetime
    updated_at: Optional[datetime] = None
    source: Optional[str] = "llm"
    source_version_id: Optional[str] = None
    source_story_text: Optional[str] = None
    source_acceptance_criteria: Optional[str] = None
    is_accepted: Optional[bool] = None
    accepted_at: Optional[datetime] = None
    eligible_for_reanalysis: Optional[bool] = None

    class Config:
        from_attributes = True


