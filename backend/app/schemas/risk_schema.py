# ============================================================

class RiskBatchCreate(BaseModel):
    """Batch creation of risks for multiple user stories."""
    risks: List[RiskCreate]


class BatchFailedItem(BaseModel):
    """Failed item in batch operation."""
    index: Optional[int] = Field(None, description="Index in the batch")
    issue_key: Optional[str] = Field(None, description="Jira issue key")
    error: str = Field(..., description="Error message")


class RiskBatchResponse(BaseModel):
    """Response for batch operations."""
    
    created: List[RiskResponse] = Field(default_factory=list)
    failed: List[BatchFailedItem] = Field(default_factory=list)
    total_success: int = Field(0, description="Number of successfully created risks")
    total_failed: int = Field(0, description="Number of failed creations")
    
    @property
    def success_rate(self) -> float:
        """Calculate success rate as percentage."""
        total = self.total_success + self.total_failed
        if total == 0:
            return 0.0
        return round(self.total_success / total * 100, 1)