# CREATE / UPDATE SCHEMAS
# ============================================================

class RiskCreate(RiskBase):
    """Schema for creating a new Risk."""

    user_story_id: str = Field(
        ..., 
        min_length=36, 
        max_length=36,
        description="ID of the User Story this risk belongs to"
    )
    test_plan_id: Optional[str] = Field(
        None,
        min_length=36,
        max_length=36,
        description="Optional link to a Test Plan"
    )
    source: Optional[str] = "llm"
    source_version_id: Optional[str] = None
    source_story_text: Optional[str] = Field(
        None,
        description="The exact story text that was analyzed"
    )
    source_acceptance_criteria: Optional[str] = Field(
        None,
        description="JSON string of acceptance criteria used for analysis"
    )


class RiskUpdate(BaseModel):
    """Schema for updating an existing Risk (all fields optional)."""
    
    description: Optional[str] = Field(None, min_length=3, max_length=5000)
    mitigation: Optional[str] = Field(None, max_length=5000)
    reasoning: Optional[str] = Field(None, max_length=10000)
    
    probability: Optional[int] = Field(
        None, 
        ge=1, 
        le=5,
        description="New probability (1-5)"
    )
    impact: Optional[int] = Field(
        None, 
        ge=1, 
        le=5,
        description="New impact (1-5)"
    )
    
    probability_factors: Optional[Dict[str, int]] = None
    probability_reasoning: Optional[str] = None
    impact_factors: Optional[Dict[str, int]] = None
    impact_reasoning: Optional[str] = None
    
    test_depth: Optional[str] = Field(
        None,
        pattern="^(comprehensive|thorough|standard|smoke)$"
    )
    
    is_ai_generated: Optional[bool] = None
    is_accepted: Optional[bool] = None
    source: Optional[str] = None
    
    @field_validator("probability")
    @classmethod
    def validate_probability(cls, v: Optional[int]) -> Optional[int]:
        """Probability must be integer 1-5 if provided."""
        if v is not None:
            if v < 1 or v > 5:
                raise ValueError("Probability must be between 1 and 5")
        return v
    
    @field_validator("impact")
    @classmethod
    def validate_impact(cls, v: Optional[int]) -> Optional[int]:
        """Impact must be integer 1-5 if provided."""
        if v is not None:
            if v < 1 or v > 5:
                raise ValueError("Impact must be between 1 and 5")
        return v


