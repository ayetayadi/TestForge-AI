"""Pydantic schemas for Risk model - Risk Based Testing (ISTQB)."""

from datetime import datetime
from typing import Optional, List, Dict, Any, Union
from pydantic import BaseModel, Field, field_validator, model_validator


# ============================================================
# BASE SCHEMA
# ============================================================
class RiskBase(BaseModel):
    """Base schema with common attributes aligned with Risk Based Testing document."""

    description: str = Field(
        ..., 
        min_length=3, 
        max_length=5000,
        description="Risk description: what can go wrong? (max 15 words recommended)"
    )
    mitigation: Optional[str] = Field(
        None, 
        max_length=5000,
        description="Testing action to reduce risk (max 12 words recommended)"
    )
    reasoning: Optional[str] = Field(
        None, 
        max_length=10000,
        description="3 bullet points: why P, why I, calculation"
    )
    
    # ── PROBABILITY (Document original: 1-5 scale) ──
    probability: int = Field(
        3, 
        ge=1, 
        le=5,
        description="Probability of failure: 1 (unlikely) to 5 (almost certain)"
    )
    
    # ── PROBABILITY FACTORS ──
    probability_factors: Optional[Dict[str, int]] = Field(
        None,
        description="P sub-factors: {story_complexity, ac_complexity, dependencies, clarity} each 1-5. P = round(avg)."
    )
    probability_reasoning: Optional[str] = Field(
        None,
        description="Why this probability? Explain the factors considered."
    )
    
    # ── IMPACT (Document original: 1-5 scale) ──
    impact: int = Field(
        3, 
        ge=1, 
        le=5,
        description="Impact severity: 1 (minimal) to 5 (business-critical)"
    )
    
    # ── IMPACT FACTORS ──
    impact_factors: Optional[Dict[str, int]] = Field(
        None,
        description="I sub-factors: {users_affected, revenue, safety, reputation} each 1-5. I = round(avg)."
    )
    impact_reasoning: Optional[str] = Field(
        None,
        description="Why this impact? Explain the factors considered."
    )
    
    test_depth: Optional[str] = Field(
        "standard",
        pattern="^(comprehensive|thorough|standard|smoke)$",
        description="Recommended test depth based on risk level"
    )
    
    # ── METADATA ──
    is_ai_generated: bool = Field(
        True,
        description="Was this risk generated by AI/LLM?"
    )
    is_accepted: Optional[bool] = Field(
        None,
        description="Has the QA lead validated this risk? None=pending, True=accepted, False=rejected"
    )
    source: Optional[str] = Field(
        "llm",
        pattern="^(ml|llm|original|approved_version|human_modified|manual)$",
        description="Source of the risk analysis (ml = P/I predicted by the KNN model)"
    )
    
    @field_validator("probability")
    @classmethod
    def validate_probability(cls, v: int) -> int:
        """Probability must be integer between 1 and 5 (document original scale)."""
        if not isinstance(v, int):
            raise ValueError("Probability must be an integer")
        if v < 1 or v > 5:
            raise ValueError("Probability must be between 1 and 5")
        return v
    
    @field_validator("impact")
    @classmethod
    def validate_impact(cls, v: int) -> int:
        """Impact must be integer between 1 and 5 (document original scale)."""
        if not isinstance(v, int):
            raise ValueError("Impact must be an integer")
        if v < 1 or v > 5:
            raise ValueError("Impact must be between 1 and 5")
        return v
    
    def compute_risk_score(self) -> int:
        """
        P × I as per Risk Based Testing document.
        Returns integer 1-25 (NOT float 0.1-4.5).
        """
        return self.probability * self.impact
    
    def compute_level(self) -> str:
        """
        ISTQB 5×5 matrix classification (Score = P × I, range 1-25):
        - Critical : 15-25
        - High     :  9-14
        - Medium   :  4-8
        - Low      :  1-3
        """
        score = self.compute_risk_score()
        if score >= 15:
            return "critical"
        if score >= 9:
            return "high"
        if score >= 4:
            return "medium"
        return "low"
    
    def compute_test_depth(self) -> str:
        """Get recommended test depth based on computed level."""
        depth_map = {
            "critical": "comprehensive",
            "high": "thorough",
            "medium": "standard",
            "low": "smoke"
        }
        return depth_map.get(self.compute_level(), "standard")


