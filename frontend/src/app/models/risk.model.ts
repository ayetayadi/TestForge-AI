// ============================================================
// TYPES
// ============================================================

export type RiskLevel = 'critical' | 'high' | 'medium' | 'low';

export type RiskSource = 
  | 'llm'                    // Generated by LLM
  | 'original'               // From original user story
  | 'approved_version'       // From approved story version
  | 'human_modified'         // LLM-generated but human-corrected
  | 'manual';                // Created manually without AI

export type TestDepth = 'comprehensive' | 'thorough' | 'standard' | 'smoke';

export type EffortAllocation = '60%' | '25%' | '10%' | '5%';

// ============================================================
// RISK MODEL
// ============================================================

export interface Risk {
  id: string;
  
  // ── Foreign Keys ──
  user_story_id: string;
  test_plan_id?: string | null;
  
  // ── Computed Properties (from joined UserStory) ──
  user_story_key?: string | null;        // Jira issue key (e.g., PROJ-123)
  user_story_title?: string | null;      // Title of the User Story
  
  // ── Source Traceability ──
  source: RiskSource;
  source_version_id?: string | null;
  source_story_text?: string | null;     // Exact text analyzed
  source_acceptance_criteria?: string | null;  // JSON string of ACs
  
  // ── Probability (1-5) ──
  probability: number;                   // 1-5: How likely is a bug?
  probability_factors?: {                // Detail factors
    complexity?: number;                 // 1-5
    change_rate?: number;                // 1-5
    dev_experience?: number;             // 1-5
    tech_debt?: number;                  // 1-5
  } | null;
  probability_reasoning?: string | null; // Why this P?
  
  // ── Impact (1-5) ──
  impact: number;                        // 1-5: How bad if bug occurs?
  impact_factors?: {                     // Detail factors
    users_affected?: number;             // 1-5
    revenue?: number;                    // 1-5
    safety?: number;                     // 1-5
    reputation?: number;                 // 1-5
  } | null;
  impact_reasoning?: string | null;      // Why this I?
  
  // ── Calculated Score ──
  risk_score: number;                    // P × I (1-25)
  level: RiskLevel;                      // critical | high | medium | low
  
  // ── Risk Description ──
  description: string;                   // What can go wrong?
  mitigation: string | null;             // How to test it?
  reasoning: string | null;              // 3 bullet points explanation
  
  // ── Test Recommendations ──
  test_depth: TestDepth;                 // comprehensive | thorough | standard | smoke
  
  // ── Human Correction Trackinag ──
  is_ai_generated: boolean;              // true = LLM, false = manual
  is_accepted: boolean | null;           // null=pending, true=accepted, false=rejected
  accepted_at?: string | null;           // When accepted/rejected
  corrected_by?: string | null;          // Who corrected it
  corrected_at?: string | null;          // When corrected
  correction_comment?: string | null;    // Reason for correction
  
  // ── Previous Values (for audit trail) ──
  previous_probability?: number | null;  // Before human correction
  previous_impact?: number | null;       // Before human correction
  previous_risk_score?: number | null;   // Before human correction
  previous_level?: RiskLevel | null;     // Before human correction
  
  // ── Timestamps ──
  created_at: string;
  updated_at?: string | null;
}

// ============================================================
// RISK SUMMARY
// ============================================================

export interface RiskSummary {
  total: number;
  by_level: Record<RiskLevel, number>;
  avg_score: number;
  max_score: number;
  min_score: number;
  accepted_count: number;
  rejected_count: number;
  pending_count: number;
  effort_distribution: Record<RiskLevel, string>;
}

// ============================================================
// RISK MATRIX
// ============================================================

export interface RiskMatrixCell {
  row: number;          // Probability row (0-4, where 0=P5)
  col: number;          // Impact column (0-4, where 0=I1)
  level: RiskLevel;
  bgScore: number;      // P × I for this cell
  risks: Risk[];        // Risks in this cell
}

