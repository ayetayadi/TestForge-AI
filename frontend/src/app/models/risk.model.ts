/**
 * Classify risk level based on score (Document Original thresholds).
 * Critical: 20-25 | High: 12-19 | Medium: 6-11 | Low: 1-5
 */
export function classifyLevel(score: number): RiskLevel {
  if (score >= 20) return 'critical';
  if (score >= 12) return 'high';
  if (score >= 6) return 'medium';
  return 'low';
}

/**
 * Map probability (1-5) to matrix row (0-4).
 * P=5 → row 0 (top), P=1 → row 4 (bottom)
 */
export function mapProbToRow(p: number): number {
  return 5 - p;
}

/**
 * Map impact (1-5) to matrix column (0-4).
 * I=1 → col 0 (left), I=5 → col 4 (right)
 */
export function mapImpactToCol(i: number): number {
  return i - 1;
}

/**
 * Calculate risk score = P × I
 */
export function calculateRiskScore(probability: number, impact: number): number {
  return probability * impact;
}

/**
 * Get test depth based on risk level
 */
export function getTestDepth(level: RiskLevel): TestDepth {
  const depthMap: Record<RiskLevel, TestDepth> = {
    critical: 'comprehensive',
    high: 'thorough',
    medium: 'standard',
    low: 'smoke',
  };
  return depthMap[level];
}


/**
 * Get color config for a risk level
 */
export function getLevelConfig(level: RiskLevel) {
  return RISK_LEVEL_CONFIG[level];
}

/**
 * Check if a risk is critical (score ≥ 20)
 */
export function isCritical(risk: Risk): boolean {
  return risk.risk_score >= 20;
}

/**
 * Check if a risk is high priority (score ≥ 12)
 */
export function isHighPriority(risk: Risk): boolean {
  return risk.risk_score >= 12;
}

/**
 * Format risk matrix position (e.g., "P4-I5")
 */
export function formatMatrixPosition(probability: number, impact: number): string {
  return `P${probability}-I${impact}`;
}

/**
 * Check if a risk can be modified by human
 */
export function canModify(risk: Risk): boolean {
  return risk.source === 'llm' || risk.source === 'human_modified';
}

/**
 * Get risk summary line (e.g., "[CRITICAL] Payment fails (P4×I5=20)")
 */
export function getRiskSummaryLine(risk: Risk): string {
  return `[${risk.level.toUpperCase()}] ${risk.description} (P${risk.probability}×I${risk.impact}=${risk.risk_score})`;
}